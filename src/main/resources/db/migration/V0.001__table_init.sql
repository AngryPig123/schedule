CREATE
    SEQUENCE TECH_STACK_SEQ
    START
        WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE
        1;

CREATE TABLE TECH_STACK
(
    TECH_STACK_ID          BIGINT    DEFAULT NEXTVAL('TECH_STACK_SEQ') PRIMARY KEY,
    TECH_STACK_CATEGORY_ID BIGINT      NOT NULL,
    TECH_STACK_NAME        VARCHAR(50) NOT NULL UNIQUE,
